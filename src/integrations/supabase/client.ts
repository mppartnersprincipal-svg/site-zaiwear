import { createClient } from '@supabase/supabase-js'

const supabaseUrl = import.meta.env.VITE_SUPABASE_URL as string
const supabaseAnonKey = import.meta.env.VITE_SUPABASE_ANON_KEY as string

if (!supabaseUrl || !supabaseAnonKey) {
  throw new Error('Missing Supabase environment variables. Check your .env file.')
}

// Use sessionStorage instead of localStorage for auth tokens.
// This prevents stale/expired tokens from persisting across browser sessions
// and blocking all Supabase queries (including public ones) on page load.
const sessionStorageAdapter = {
  getItem: (key: string) => sessionStorage.getItem(key),
  setItem: (key: string, value: string) => sessionStorage.setItem(key, value),
  removeItem: (key: string) => sessionStorage.removeItem(key),
}

// Clear any leftover localStorage tokens from previous config
Object.keys(localStorage)
  .filter(k => k.startsWith('sb-'))
  .forEach(k => localStorage.removeItem(k))

export const supabase = createClient(supabaseUrl, supabaseAnonKey, {
  auth: {
    autoRefreshToken: true,
    persistSession: true,
    detectSessionInUrl: false,
    storage: sessionStorageAdapter,
  },
})
